    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        instaAccounts = new ArrayList<>();
        try {
            for (ScreenAccount account : helpTable.getAll()) {
                if (account.isSelected()) {
                    if (account.isInsta()) {
                        InstagramAccount insta = instagramAccountDao.findByUserName(account.getUsername());
                        insta.connect();
                        instaAccounts.add(insta);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        if(instaAccounts.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Choose at least one account to start");
            return;
        }
        lastTime = LocalDateTime.now();
        txtStartTime.setText(lastTime.format(DateTimeFormatter.ISO_DATE_TIME).toString());
        btnStop.setEnabled(true);
        btnStart.setEnabled(false);
        bot = new Bot();
        bot.start();
    }//GEN-LAST:event_btnStartActionPerformed

