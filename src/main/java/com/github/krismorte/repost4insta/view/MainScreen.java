    private class Bot extends Thread {

        public boolean status;
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");

        @Override
        public void run() {
            status = true;
            while (status) {
                try {
                    for (InstagramAccount account : instaAccounts) {
                        List<AccountTarget> targets = accountTargetDao.listByAccount(account.getUsername());
                        for (AccountTarget target : targets) {
                            TimeLine timeLine = account.getUserTimeline(target.getTargetId());
                            for (InstagramFeedItem feed : timeLine.getFeeds().getItems()) {
                                List<StatusReposted> feeds = statusRepostedDao.listByAccount(account.getUsername());
                                if (feeds.isEmpty()) {
                                    account.repost(Post.instaPost(feed));
                                    saveStatus(account.getUsername(), feed.getPk());
                                } else {
                                    if (feeds.stream().filter(t -> t.getPostId() == feed.getPk()).collect(Collectors.toList()).isEmpty()) {
                                        account.repost(Post.instaPost(feed));
                                        saveStatus(account.getUsername(), feed.getPk());
                                    }
                                }
                                Thread.sleep(500);
                            }
                        }
                    }
                    Thread.sleep(180000);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }

    }

    private void saveStatus(String userName, long postId) {
        StatusReposted statusReposted = new StatusReposted(userName, postId);
        statusRepostedDao.doSave(statusReposted);
    }

